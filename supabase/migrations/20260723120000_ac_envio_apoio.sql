-- ─── Bucket privado p/ as requisições digitalizadas ──────────────────────────────
-- O SPA sobe os arquivos direto (authenticated) e passa os paths para a API rodar o
-- OCR; leitura via signed URL. PRIVADO: são documentos médicos com dados de paciente.
INSERT INTO storage.buckets (id, name, public)
VALUES ('ac-apoio-requisicoes', 'ac-apoio-requisicoes', false)
ON CONFLICT (id) DO UPDATE SET public = false;

DROP POLICY IF EXISTS "ac_apoio_requisicoes_storage_insert" ON storage.objects;
